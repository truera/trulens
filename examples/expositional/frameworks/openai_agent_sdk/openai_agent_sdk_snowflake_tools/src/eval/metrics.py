_analyst_gt: dict[str, str] | None = None


def _get_analyst_gt() -> dict[str, str]:
    global _analyst_gt
    if _analyst_gt is None:
        _analyst_gt = load_analyst_golden_sql(get_snowpark_session())
    return _analyst_gt


def _extract_sql(tool_output: str) -> str | None:
    match = re.search(r"SQL:\s*(.+?)(?:\n\s*Results:|\Z)", tool_output, re.DOTALL)
    if match:
        sql = match.group(1).strip().rstrip(";")
        sql = re.sub(r"\s*--\s*Generated by Cortex Analyst.*$", "", sql, flags=re.MULTILINE).strip()
        return sql
    return None


def _normalize_sql(sql: str) -> str:
    return " ".join(sql.split())


def _format_diagnostics(result) -> str:
    sections = []
    if result.err:
        sections.append("DIFFERENCES:\n" + "\n".join(f"  • {e}" for e in result.err))
    if result.warn:
        sections.append("WARNINGS:\n" + "\n".join(f"  • {w}" for w in result.warn))
    if result.info:
        sections.append("COLUMN MAPPING:\n" + "\n".join(f"  • {i}" for i in result.info))
    return "\n\n".join(sections) if sections else "No diagnostics."


def _format_results_table(label: str, df) -> str:
    row_count = len(df)
    col_count = len(df.columns)
    preview = df.head(5)
    col_names = [str(c) for c in preview.columns]

    lines = [f"{label} ({row_count} rows, {col_count} cols):"]
    lines.append("-" * 40)
    for idx, (_, row) in enumerate(preview.iterrows(), 1):
        lines.append(f"  Row {idx}:")
        for col, val in zip(col_names, row):
            lines.append(f"    {col}: {val}")
        if idx < len(preview):
            lines.append("")
    lines.append("-" * 40)
    if row_count > 5:
        lines.append(f"  ... ({row_count - 5} more rows)")
    return "\n".join(lines)


def sql_agreement_golden(question: str, sql_output: str) -> tuple[float, dict]:
    gt = _get_analyst_gt()
    golden_sql = gt.get(question)
    if not golden_sql:
        return float("nan"), {"reason": "No golden SQL found for this question."}

    predicted_sql = _extract_sql(sql_output)
    if not predicted_sql:
        return 0.0, {
            "reason": (
                "\u2718 NO SQL EXTRACTED\n\n"
                "Could not find SQL in agent output.\n"
                f"Expected format: SQL: <statement>\n\n"
                f"Agent output (first 300 chars):\n{sql_output[:300]}"
            )
        }

    pred_normalized = _normalize_sql(predicted_sql)
    gold_normalized = _normalize_sql(golden_sql)

    session = get_snowpark_session()
    try:
        gold_df = session.sql(golden_sql).to_pandas()
    except Exception as e:
        return float("nan"), {
            "reason": (
                f"\u2718 GOLDEN SQL EXECUTION ERROR\n\n"
                f"Error: {e}\n\n"
                f"Golden SQL:\n  {gold_normalized}"
            )
        }

    try:
        pred_df = session.sql(predicted_sql).to_pandas()
    except Exception as e:
        return 0.0, {
            "reason": (
                f"\u2718 PREDICTED SQL EXECUTION ERROR\n\n"
                f"Error: {e}\n\n"
                f"Predicted SQL:\n  {pred_normalized}\n\n"
                f"Golden SQL:\n  {gold_normalized}"
            )
        }

    result = compare_dataframes(gold_df, pred_df, model="openai-gpt-5.1")
    diag = _format_diagnostics(result)

    if result.result == Equality.EQUAL:
        verdict = "PASS"
        score = 1.0
    elif result.result == Equality.UNCLEAR:
        verdict = "UNCLEAR"
        score = 0.5
    else:
        verdict = "FAIL"
        score = 0.0

    icon = "\u2714" if score == 1.0 else ("\u25cb" if score == 0.5 else "\u2718")
    parts = [f"{icon} {verdict}"]
    parts.append(f"\nPredicted SQL:\n  {pred_normalized}")
    parts.append(f"\nGolden SQL:\n  {gold_normalized}")

    if score < 1.0:
        parts.append(f"\n{diag}")
        parts.append(f"\n{_format_results_table('Expected', gold_df)}")
        parts.append(f"\n{_format_results_table('Actual', pred_df)}")
    else:
        if diag != "No diagnostics.":
            parts.append(f"\n{diag}")

    return score, {"reason": "\n".join(parts)}


def _truncate(text: str, limit: int = 100) -> str:
    return text[:limit] + "..." if len(text) > limit else text


def _precision_at_k_with_reason(
    gt_agreement: GroundTruthAgreement,
    query: str,
    retrieved_context_chunks: List[str],
) -> tuple[float, dict]:
    ground_truth_chunks = gt_agreement._find_golden_context_chunks_and_scores(query)
    if not ground_truth_chunks:
        return float("nan"), {"reason": "No ground truth context found for this query."}

    golden_texts = set(chunk[0] for chunk in ground_truth_chunks)
    retrieved_top_k = retrieved_context_chunks

    hits = [c for c in retrieved_top_k if c in golden_texts]
    misses = [c for c in retrieved_top_k if c not in golden_texts]
    score = len(hits) / len(retrieved_top_k) if retrieved_top_k else 0.0

    verdict = "PASS" if score == 1.0 else ("PARTIAL" if score > 0 else "FAIL")
    icon = "\u2714" if score == 1.0 else ("\u25cb" if score > 0 else "\u2718")
    parts = [
        f"{icon} {verdict}  Precision@{len(retrieved_top_k)} = {len(hits)}/{len(retrieved_top_k)} = {score:.2f}",
    ]
    if hits:
        parts.append(f"\nRelevant Retrieved ({len(hits)}):")
        for h in hits:
            parts.append(f"  \u2714 {_truncate(h)}")
    if misses:
        parts.append(f"\nIrrelevant Retrieved ({len(misses)}):")
        for m in misses:
            parts.append(f"  \u2718 {_truncate(m)}")
    parts.append(f"\nExpected ({len(golden_texts)}):")
    for gt in golden_texts:
        parts.append(f"  \u2022 {_truncate(gt)}")

    return score, {"reason": "\n".join(parts)}


def _recall_at_k_with_reason(
    gt_agreement: GroundTruthAgreement,
    query: str,
    retrieved_context_chunks: List[str],
) -> tuple[float, dict]:
    ground_truth_chunks = gt_agreement._find_golden_context_chunks_and_scores(query)
    if not ground_truth_chunks:
        return float("nan"), {"reason": "No ground truth context found for this query."}

    golden_texts = set(chunk[0] for chunk in ground_truth_chunks)
    retrieved_top_k = retrieved_context_chunks

    found = golden_texts & set(retrieved_top_k)
    missed = golden_texts - set(retrieved_top_k)
    score = len(found) / len(golden_texts) if golden_texts else 0.0

    verdict = "PASS" if score == 1.0 else ("PARTIAL" if score > 0 else "FAIL")
    icon = "\u2714" if score == 1.0 else ("\u25cb" if score > 0 else "\u2718")
    parts = [
        f"{icon} {verdict}  Recall@{len(retrieved_top_k)} = {len(found)}/{len(golden_texts)} = {score:.2f}",
    ]
    if found:
        parts.append(f"\nGround Truth Found ({len(found)}):")
        for f_ in found:
            parts.append(f"  \u2714 {_truncate(f_)}")
    if missed:
        parts.append(f"\nGround Truth Missed ({len(missed)}):")
        for m in missed:
            parts.append(f"  \u2718 {_truncate(m)}")

    return score, {"reason": "\n".join(parts)}


def build_metrics(snowpark_session: Session) -> list[Metric]:
    provider = Cortex(
        snowpark_session=snowpark_session,
        model_engine="openai-gpt-5.1",
    )

    m_sql_agreement = (
        Metric(implementation=sql_agreement_golden, name="SQL Agreement (Golden)")
        .on_input()
        .on({
            "sql_output": Selector(
                span_type=SpanAttributes.SpanType.TOOL,
                span_attribute=SpanAttributes.CALL.RETURN,
                collect_list=False,
            ),
        })
    )

    search_gt_data = load_search_ground_truth(snowpark_session)
    gt_agreement = GroundTruthAgreement(
        ground_truth=search_gt_data,
        provider=provider,
    )

    def precision_with_reason(query: str, retrieved_context_chunks: List[str]):
        return _precision_at_k_with_reason(gt_agreement, query, retrieved_context_chunks)

    def recall_with_reason(query: str, retrieved_context_chunks: List[str]):
        return _recall_at_k_with_reason(gt_agreement, query, retrieved_context_chunks)

    m_precision = (
        Metric(implementation=precision_with_reason, name="Precision@k")
        .on_input()
        .on({
            "retrieved_context_chunks": Selector(
                span_type=SpanAttributes.SpanType.RETRIEVAL,
                span_attribute=SpanAttributes.RETRIEVAL.RETRIEVED_CONTEXTS,
                collect_list=True,
            ),
        })
    )

    m_recall = (
        Metric(implementation=recall_with_reason, name="Recall@k")
        .on_input()
        .on({
            "retrieved_context_chunks": Selector(
                span_type=SpanAttributes.SpanType.RETRIEVAL,
                span_attribute=SpanAttributes.RETRIEVAL.RETRIEVED_CONTEXTS,
                collect_list=True,
            ),
        })
    )

    return [m_sql_agreement, m_precision, m_recall]
